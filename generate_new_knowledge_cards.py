# 生成并插入新的知识卡片
def generate_new_cards():
    # 获取数据库会话
    db_gen = get_db()
    db = next(db_gen)
    
    try:
        # 统计现有数据
        existing_count = db.query(KnowledgeCard).count()
        print(f"数据库中现有知识卡片数量: {existing_count}")
        
        # 生成新的知识卡片
        new_cards = []
        for category, cards in new_knowledge_data.items():
            for card in cards:
                # 生成拼音图片名（暂时为空，等待用户添加图片后再更新）
                # 图片名将命名为标题的拼音，例如 "井干式结构" -> "jingganshijiegou.png"
                image = None  # 暂时为空，后续会更新
                
                new_cards.append(KnowledgeCard(
                    title=card['title'],
                    category=category,
                    image=image,
                    content=card['content'],
                    tags=card['tags']
                ))
        
        # 批量插入
        db.bulk_save_objects(new_cards)
        db.commit()
        
        # 统计新数据
        new_count = db.query(KnowledgeCard).count()
        print(f"成功生成 {len(new_cards)} 条新的知识卡片")
        print(f"现在数据库中共有 {new_count} 条知识卡片")
        
        # 打印生成的知识卡片列表
        print("\n生成的新知识卡片：")
        for card in new_cards:
            print(f"- {card.title} ({card.category}) - 图片名建议：{card.title.replace(' ', '')}.png")
        
    except Exception as e:
        db.rollback()
        print(f"生成知识卡片时出错: {e}")
    finally:
        db.close()

if __name__ == "__main__":
    generate_new_cards()
