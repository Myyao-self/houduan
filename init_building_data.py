def init_buildings():
    # 初始化数据库
    init_db()
    
    # 获取数据库会话
    db_gen = get_db()
    db = next(db_gen)
    
    try:
        # 检查是否已有数据
        existing_buildings = db.query(Building).count()
        if existing_buildings > 0:
            print(f"数据库中已有 {existing_buildings} 条建筑数据，跳过初始化")
            return
        
        # 导入数据
        buildings_to_create = []
        for building in building_data:
            buildings_to_create.append(Building(
                title=building['title'],
                brief=building['brief'],
                detail=building['detail'],
                image=building['image'],
                tags=building['tags'],
                type=building['type']
            ))
        
        # 批量插入
        db.bulk_save_objects(buildings_to_create)
        db.commit()
        
        print(f"成功导入 {len(buildings_to_create)} 条建筑分类数据")
        
    except Exception as e:
        db.rollback()
        print(f"导入建筑分类数据时出错: {e}")
    finally:
        db.close()

if __name__ == "__main__":
    init_buildings()
