def init_knowledge_cards():
    # 初始化数据库
    init_db()
    
    # 获取数据库会话
    db_gen = get_db()
    db = next(db_gen)
    
    try:
        # 检查是否已有数据
        existing_cards = db.query(KnowledgeCard).count()
        if existing_cards > 0:
            print(f"数据库中已有 {existing_cards} 条知识卡片数据，跳过初始化")
            return
        
        # 导入数据
        cards_to_create = []
        for category, cards in knowledge_data.items():
            for card in cards:
                cards_to_create.append(KnowledgeCard(
                    title=card['title'],
                    category=category,
                    image=card['image'],
                    content=card['content'],
                    tags=card['tags']
                ))
        
        # 批量插入
        db.bulk_save_objects(cards_to_create)
        db.commit()
        
        print(f"成功导入 {len(cards_to_create)} 条知识卡片数据")
        
    except Exception as e:
        db.rollback()
        print(f"导入知识卡片数据时出错: {e}")
    finally:
        db.close()

if __name__ == "__main__":
    init_knowledge_cards()
